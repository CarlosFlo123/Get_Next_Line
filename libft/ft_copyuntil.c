#include "libft.h"

int			ft_copyuntil(char **dst, char *src, char c)
{
	int		i;
	int		count;
	int		pos;
	char	*tmp;

	i = -1;
	count = 0;
	while (src[++i])
		if (src[i] == c)
			break ;
	pos = i;
	if (!(*dst = ft_strnew(i)))
		return (0);
	while (src[count] && count < i)
	{
		tmp = *dst;
		*dst = NULL;
		free(*dst);
		if (!(*dst = ft_strjoinch(tmp, src[count])))
			return (0);
		count++;
	}
	return (pos);
}
